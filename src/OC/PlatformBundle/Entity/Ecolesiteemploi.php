<?php

namespace OC\PlatformBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Ecolesiteemploi
 *
 * @ORM\Table(name="ecolesiteemploi")
 * @ORM\Entity
 */
class Ecolesiteemploi
{
    /**
     * @var integer
     *
     * @ORM\Column(name="Id_EcoleSiteEmploi", type="integer", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $idEcolesiteemploi;

    /**
     * @var integer
     *
     * @ORM\Column(name="Id_Ecole", type="integer", nullable=false)
     */
    private $idEcole;

    /**
     * @var integer
     *
     * @ORM\Column(name="Id_SiteEmploi", type="integer", nullable=false)
     */
    private $idSiteemploi;



    /**
     * Get idEcolesiteemploi
     *
     * @return integer
     */
    public function getIdEcolesiteemploi()
    {
        return $this->idEcolesiteemploi;
    }

    /**
     * Set idEcole
     *
     * @param integer $idEcole
     *
     * @return Ecolesiteemploi
     */
    public function setIdEcole($idEcole)
    {
        $this->idEcole = $idEcole;

        return $this;
    }

    /**
     * Get idEcole
     *
     * @return integer
     */
    public function getIdEcole()
    {
        return $this->idEcole;
    }

    /**
     * Set idSiteemploi
     *
     * @param integer $idSiteemploi
     *
     * @return Ecolesiteemploi
     */
    public function setIdSiteemploi($idSiteemploi)
    {
        $this->idSiteemploi = $idSiteemploi;

        return $this;
    }

    /**
     * Get idSiteemploi
     *
     * @return integer
     */
    public function getIdSiteemploi()
    {
        return $this->idSiteemploi;
    }
}
